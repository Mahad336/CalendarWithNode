const mongoose = require("mongoose");

const smoothieSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    snippet: {
      type: String,
      required: true,
    },
    body: {
      type: String,
      required: true,
    },
    createdBy: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Smoothie = mongoose.model("Smoothie", smoothieSchema);
module.exports = Smoothie;
